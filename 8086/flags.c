void
cpu_setZF( dword ans )
{
	cpu.ZF = ( ans == 0 );
}

void
cpu_setSF( dword ans, byte bits )
{
	if( bits == 16 )
		cpu.SF = ( ans >> 15 ) & 1;
	else
		cpu.SF = ( ans >> 7 ) & 1;
}

void
cpu_setPF( dword ans )
{
    cpu.PF = parity_table[ans & 0xFF];
}

void
cpu_setAF( dword ans, word src, word dest )
{
	cpu.AF = ( ( ( ans ^ ( src ^ dest ) ) & 0x10 ) >> 4) & 1;
}
